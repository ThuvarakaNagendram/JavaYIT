class PrimitiveDataTypes_06{
	public static void main(String args[])
	{
		Byte b1=12;
		System.out.println("Byte: "+b1);
		
		Short s1=32000;
		System.out.println("Short: "+s1);
		
		int i=65022;
		System.out.println("Int: "+i);
		
		long l=650226558656L;
		System.out.println("Long: "+l);
		
		float f1=25.6f;
		System.out.println("Float: "+f1);
		
		double db=86.35D;
		System.out.println("Double: "+db);
		
		boolean bool=true;
		System.out.println("Boolean: "+bool);
		
		char ch='A';
		System.out.println("Char: "+ch);
		
		char ch1=64;
		System.out.println("Char decimal: "+ch1);
		
		char ch2=0101;
		System.out.println("Char octal value: "+ch2);
		
		char ch3=0*41;
		System.out.println("Char hexa decimal: "+ch3);
		
		char ch4=0b1000001;
		System.out.println("Char binary: "+ch4);
		
		char ch5='\u0041';
		System.out.println("Char unicode: "+ch5);
	}
}