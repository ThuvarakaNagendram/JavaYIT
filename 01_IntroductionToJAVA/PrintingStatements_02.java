class PrintingStatements_02{
	public static void main (String args[]) {
	System.out.println("My name is Thuvaraka");
	System.out.println("My father name is Nagendram");
	System.out.println("My mother name is Ratharamany");
	
	System.out.println("Above used PRINTLN to print");
	
	System.out.print("My name is Thuvaraka\n");
	System.out.print("My father name is Nagendram\n");
	System.out.print("My mother name is Ratharamany\n");
	
	System.out.println("Above used backward n to print");
	
	System.out.print("My name is Thuvaraka");
	System.out.print("My father name is Nagendram");
	System.out.print("My mother name is Ratharamany");
	
	}
}