class ArithmeticOperators{
	public static void main (String args[]){
		int x = 64;
		int y = 2;
		System.out.println("X= "+x);
		System.out.println("Y= "+y);
		
		System.out.println("x+y = "+ (x+y));
		System.out.println("x-y = "+ (x-y));
		System.out.println("x*y = "+ (x*y));
		System.out.println("x/y = "+ (x/y));
		System.out.println("x%y = "+ (x%y));
	}
}